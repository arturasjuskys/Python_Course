#	Coding Exercise 32: State Abbreviations Exercise



list1 = ["CA", "NJ", "RI"]
list2 = ["California", "New Jersey", "Rhode Island"]

answer1 = {list1[index]: list2[index] for index in range(len(list1))}
print(answer1)
# {'CA': 'California', 'NJ': 'New Jersey', 'RI': 'Rhode Island'}



