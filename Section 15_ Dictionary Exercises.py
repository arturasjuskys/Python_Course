#	Coding Exercise 33: List to Dictionary Exercise



person = [["name", "Jared"], ["job", "Musician"], ["city", "Bern"]]
answer2 = {index[0]: index[1] for index in person}
print(answer2)
# {'name': 'Jared', 'job': 'Musician', 'city': 'Bern'}



person = [["name", "Jared"], ["job", "Musician"], ["city", "Bern"]]
answer3 = {key: value for key,value in person}
print(answer3)
# {'name': 'Jared', 'job': 'Musician', 'city': 'Bern'}



person = [["name", "Jared"], ["job", "Musician"], ["city", "Bern"]]
answer4 = dict(person)
print(answer4)
# {'name': 'Jared', 'job': 'Musician', 'city': 'Bern'}



